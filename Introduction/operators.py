# Arithmetic Operators

# Addition symbol
x = 2 + 2
print(x)

# Subtraction Symbol
y = 5 - 2
print(y)

# Division Symbol
z = 10 / 3
print(z)

# Modulus Symbol
a = 10 % 3
print(a)

# Multipication Symbol
b = 5 * 2
print (b)

#Float division
c = 5 // 2
print(c)


# LOGICAL OPERATORS

# AND operator
i = 5 and 5
print(i)

#OR operator
j = 10 or 4
print(j)

#NOT operator
k = not 11
print(k)


